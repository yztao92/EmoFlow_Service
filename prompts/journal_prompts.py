"""
日记生成prompt管理（事件记录优先 + 去AI味优化版）
生成更贴近真实日常记录的心情日记
"""

def get_journal_generation_prompt(emotion: str, chat_history: str) -> str:
    """
    根据情绪状态获取日记生成prompt（事件优先 · 防虚构）
    """

    prompt = f"""# 心情日记生成任务（事件优先 · 防虚构 · 仅基于用户发言）

## 任务目标
基于「对话内容参考」中 **user 的发言**，只写一篇贴近日常的心情日记。  
assistant 的内容仅作语境参考，**不得当作事实引用**或加入日记中。  
以**具体事件/动作/场景**为主，**情绪为辅**。禁止编造。

## 写作流程（内化执行，不要显式输出步骤）
1) 从用户发言中**只提取已出现或能被明确推断的事实**（事件、动作、物品、地点、人物、感受）。  
2) 若事实不足以写满，也**不得新增具体细节**；允许使用**模糊但不虚构**的衔接词（如"今天"、"路上"、"中途"）。  
3) 以第一人称口吻，用口语化短句，像在记流水账。

## 严格边界（必须遵守）
- **信息来源**仅限用户发言，不得引用 assistant 的建议或评论。  
- **不得出现**对话中未提到的具体人名/地名/数字/品牌/专业结论。  
- **不要出现**"AI"、"聊天"、"问题"、"他说/你说"等对话痕迹。  
- **不要写时间日期**（如"2025年10月16日"、"周四"）。  
- 字数 **80–100 字**；不要写作散文或鸡汤。

## 弱信息兜底（很重要）
- 如果能提取的事实 **少于 2 条**，请输出一版**极简日记**（50–70 字）：  
  - 结构：**一句状态** + **一件小事/细节** + **一句感受**；  
  - 同样**不得虚构**，用**模糊表述**保持真实感（如"中途"、"路上"、"简单处理了下"）。

## 轻量提示（自动推理用）
请先在脑海中简要判断：  
- 用户发言的核心事件是什么？  
- 用户此刻的情绪倾向？  
- 哪种表达最自然？  
然后只输出最终的日记正文。

## 对话内容参考
{chat_history}

## 输出格式
只输出日记正文，不要标题、不要步骤、不要引用、不要代码块。
"""

    return prompt


